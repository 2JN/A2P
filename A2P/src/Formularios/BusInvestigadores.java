    private void aceptar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptar1ActionPerformed
        // TODO add your handling code here:
        
         try{
        int Dpi = Integer.parseInt(JOptionPane.showInputDialog(this, "Ingrese Numero de DPI ", "Modificar",JOptionPane.QUESTION_MESSAGE));

        if(Dpi == 0){
           return;
       }
            
        Connection miConexion = (Connection) Enlace.GetConnection();
        try {
            Statement statement = (Statement) miConexion.createStatement();

            
            String FechaInicio= finicio.getText();
            String Nombre = nombre.getText();
            String Apellido = apellido.getText();
            String Direccion = direccion.getText();
            int Telefono= Integer.parseInt(telefono.getText());
            String Email = email.getText();
            String GradoAcademico = gacademico.getText(); 
            String Especialidad = especialidad.getText();
            PreparedStatement pstm = Enlace.GetConnection().prepareStatement(" UPDATE investigador "
                    
                        
                       
                       + " SET fechaInicioLabor = ? ,"
                       + " gradoAcademico = ? ,"
                       + "especialidad = ?, "
                       + "nombre = ? ,"
                       + "apellido = ?, "
                       + "direccion = ? ,"
                       + "telefono = ?, "
                       + "email = ? "
                       + "WHERE dpi = ? ");

            pstm.setString(1, FechaInicio);
            pstm.setString(2, GradoAcademico);
            pstm.setString(3, Especialidad);
            pstm.setString(4, Nombre);
            pstm.setString(5, Apellido);
            pstm.setString(6, Direccion);
            pstm.setInt(7, Telefono);
            pstm.setString(8, Email);
            pstm.setInt(9, Dpi);
            
            
            JOptionPane.showMessageDialog(this, "Datos Modificados correctamente");
            pstm.execute();
            pstm.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error " + ex.getMessage());
        }
         
         }catch (Exception e){
            
        }  
    }//GEN-LAST:event_aceptar1ActionPerformed

